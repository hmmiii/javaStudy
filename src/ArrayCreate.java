import java.util.*;
public class ArrayCreate {
    public static void main(String[] args) {
        int[] intArr = {10, 20, 30, 40, 50};

        System.out.println(Arrays.toString(intArr)); // [10, 20, 30, 40, 50]

        char[] chArr = {'a', 'b', 'c', 'd'};

        System.out.println(chArr); // abcd
    }
}
